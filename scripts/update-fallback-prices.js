/* update-fallback-prices.js — keeps src/Objects/FallbackPrices.js in sync.
 *
 * Runs automatically before every build (see package.json "prebuild"). It
 * fetches the live /membership-info, compares it to the committed fallback,
 * and rewrites the file ONLY when the price data actually changed. If the
 * endpoint is unreachable it leaves the existing fallback untouched and does
 * NOT fail the build — so a blocked/offline build still ships the last-known
 * good prices.
 */
const fs = require('fs');
const path = require('path');

// --- config (per tenant) ---
const ENDPOINT = 'https://worker-consolidated.maxli5004.workers.dev/membership-info';
const HEADERS = { 'Content-Type': 'application/json', 'x-maple': 'true' };
const OUT_FILE = path.join(__dirname, '..', 'src', 'Objects', 'FallbackPrices.js');
const BANNER_NAME = 'Maple';
const TIMEOUT_MS = 10000;

function render(data) {
  return (
    `// ${BANNER_NAME}FallbackPrices.js\n` +
    `// Static snapshot of /membership-info, bundled so the pricing section renders\n` +
    `// instantly and never goes blank if the live API request is blocked (ad\n` +
    `// blockers, network firewalls, offline). The live fetch overrides this on\n` +
    `// success. Auto-generated by scripts/update-fallback-prices.js (prebuild).\n` +
    `\n` +
    `const FALLBACK_PRICES = ${JSON.stringify(data, null, 2)};\n` +
    `\n` +
    `export default FALLBACK_PRICES;\n`
  );
}

function existingData() {
  try {
    const txt = fs.readFileSync(OUT_FILE, 'utf8');
    const m = txt.match(/const FALLBACK_PRICES = ([\s\S]*?);\s*\nexport default/);
    return m ? JSON.parse(m[1]) : null;
  } catch {
    return null;
  }
}

async function main() {
  let data;
  try {
    const ctrl = new AbortController();
    const timer = setTimeout(() => ctrl.abort(), TIMEOUT_MS);
    const res = await fetch(ENDPOINT, { headers: HEADERS, signal: ctrl.signal });
    clearTimeout(timer);
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    data = await res.json();
    if (!Array.isArray(data) || !data.length) throw new Error('empty/invalid payload');
  } catch (err) {
    console.warn(`[fallback-prices] could not reach live API (${err.message}); keeping existing fallback.`);
    return; // never break the build
  }

  if (JSON.stringify(existingData()) === JSON.stringify(data)) {
    console.log('[fallback-prices] already up to date — no change.');
    return;
  }

  fs.writeFileSync(OUT_FILE, render(data));
  console.log('[fallback-prices] prices changed — FallbackPrices.js updated.');
}

main();
